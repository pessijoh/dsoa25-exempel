public class Clothing {
    String name;
    String type;
    String color;
    String price;
    String brand;

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getColor() {
        return color;
    }
    public String getPrice() {
        return price;
    }
    public String getBrand () {
        return brand;

    }
    public void setName(String name) {
        this.name = name;

    }
}